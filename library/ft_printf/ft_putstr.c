#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	i;	

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (str[i])
	{
		ft_putchar(str[i]);
		i ++;
	}
	return (i);
}
